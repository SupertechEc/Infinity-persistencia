package ec.com.infinity.modelo2;

/**
 *
 * @author Paul
 */
public class ParametrosSOAP {

    private int cbtcodcom;

    private String clave;

    private int cbtfecemi;

    private String cbtcoddep;
    
    private String codigoComercializadora;

    /**
     * @return the cbtcodcom
     */
    public int getCbtcodcom() {
        return cbtcodcom;
    }

    /**
     * @param cbtcodcom the cbtcodcom to set
     */
    public void setCbtcodcom(int cbtcodcom) {
        this.cbtcodcom = cbtcodcom;
    }

    /**
     * @return the clave
     */
    public String getClave() {
        return clave;
    }

    /**
     * @param clave the clave to set
     */
    public void setClave(String clave) {
        this.clave = clave;
    }

    /**
     * @return the cbtfecemi
     */
    public int getCbtfecemi() {
        return cbtfecemi;
    }

    /**
     * @param cbtfecemi the cbtfecemi to set
     */
    public void setCbtfecemi(int cbtfecemi) {
        this.cbtfecemi = cbtfecemi;
    }

    /**
     * @return the cbtcoddep
     */
    public String getCbtcoddep() {
        return cbtcoddep;
    }

    /**
     * @param cbtcoddep the cbtcoddep to set
     */
    public void setCbtcoddep(String cbtcoddep) {
        this.cbtcoddep = cbtcoddep;
    }

    /**
     * @return the codigoComercializadora
     */
    public String getCodigoComercializadora() {
        return codigoComercializadora;
    }

    /**
     * @param codigoComercializadora the codigoComercializadora to set
     */
    public void setCodigoComercializadora(String codigoComercializadora) {
        this.codigoComercializadora = codigoComercializadora;
    }
}
